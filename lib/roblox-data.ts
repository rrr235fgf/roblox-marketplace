// وظيفة للبحث عن مستخدم حسب الاسم
export function searchUserByName(username: string) {
  // البحث بغض النظر عن حالة الأحرف
  const normalizedUsername = username.toLowerCase()

  // البحث في قائمة المستخدمين المحلية
  const user = robloxUsers.find(
    (user) => user.name.toLowerCase() === normalizedUsername || user.displayName.toLowerCase() === normalizedUsername,
  )

  return user
}

// وظيفة لإنشاء مستخدم جديد بناءً على اسم المستخدم المدخل
export function generateUserFromName(username: string) {
  // إنشاء معرف فريد
  const id = Math.floor(Math.random() * 1000000).toString()

  // إنشاء تاريخ إنشاء عشوائي (بين 2006 و 2023)
  const startYear = 2006
  const endYear = 2023
  const year = startYear + Math.floor(Math.random() * (endYear - startYear + 1))
  const month = Math.floor(Math.random() * 12) + 1
  const day = Math.floor(Math.random() * 28) + 1
  const created = `${year}-${month.toString().padStart(2, "0")}-${day.toString().padStart(2, "0")}T00:00:00.000Z`

  // إنشاء أرقام عشوائية للإحصائيات
  const friendsCount = Math.floor(Math.random() * 200)
  const followersCount = Math.floor(Math.random() * 10000)
  const followingCount = Math.floor(Math.random() * 100)
  const placeVisits = Math.floor(Math.random() * 1000000)

  // إنشاء حالة عشوائية
  const presenceTypes = [0, 1, 2, 3] // 0: غير متصل، 1: متصل، 2: يلعب، 3: في الاستوديو
  const presenceType = presenceTypes[Math.floor(Math.random() * presenceTypes.length)]
  const locations = ["Offline", "Online", "Playing Adopt Me!", "Playing Jailbreak", "Creating in Studio"]
  const location = locations[Math.min(presenceType, locations.length - 1)]

  // إنشاء شارات عشوائية
  const allBadges = ["لاعب", "مطور", "مصمم", "مبتدئ", "محترف", "هاوي", "مبدع"]
  const badgeCount = Math.floor(Math.random() * 3) + 1 // 1-3 شارات
  const badges = []

  for (let i = 0; i < badgeCount; i++) {
    const randomBadge = allBadges[Math.floor(Math.random() * allBadges.length)]
    if (!badges.includes(randomBadge)) {
      badges.push(randomBadge)
    }
  }

  // إنشاء كائن المستخدم
  return {
    id,
    name: username,
    displayName: username,
    description: `مرحباً! أنا ${username} وأنا أحب لعب روبلوكس.`,
    created,
    isBanned: false,
    avatar: `/placeholder.svg?height=420&width=420&text=${encodeURIComponent(username)}`,
    presence: { userPresenceType: presenceType, lastLocation: location },
    premium: Math.random() > 0.7, // 30% احتمالية أن يكون مستخدم بريميوم
    friendsCount,
    followersCount,
    followingCount,
    placeVisits,
    badges,
  }
}
